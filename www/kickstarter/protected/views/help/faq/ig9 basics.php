    <li class="faq">
      <h4 id="WhatIsKick">IG9 là gì?</h4>
      <div class="faq_answer">
        <p>IG9 là nơi dành cho các cá nhân hoặc tổ chức, doanh nghiệp thu hút vốn đầu tư và  xây dựng hình ảnh cho dự án của mình. </p>

        <p>Chúng tôi là một cộng đồng hợp tác, nơi mọi người có thể cùng tới với nhau để cùng đóng góp tiền của, trao đổi những phản hồi, ý kiến và ủng hộ cho những ý tưởng và đam mê của những  doanh nhân, nghệ sĩ, nhà văn, nhạc sĩ… có nhiệt huyết và tham vọng. </p>

        <p>Chúng tôi là nền tảng tài trợ đám đông (crowfunding) đầu tiên tại Việt Nam, nơi những dự án thú vị trở thành hiện thực bởi sự giúp đỡ của hàng triệu người.</p>
      </div>
    </li>
    <li class="faq">
      <h4 id="WhouseIg9">Những người sử dụng IG9 là ai?</h4>
      <div class="faq_answer">
        <p>Những doanh nhân, những người cần vốn tài trợ để phát triển nhưng gặp phải khó khăn từ  những điều kiện tài trợ khắt khe ở Việt Nam.</p>

        <p>Những nhà văn, tác giả với những cuốn sách ấp ủ nhiều điều muốn nói nhưng không có được sự ủng hộ từ các nhà xuất bản.</p>

        <p>Những ca sĩ, nghệ sĩ tài năng với những tác phẩm tuyệt vời nhưng không thể chi trả cho một không gian triển lãm hay tổ chức một show trình diễn.</p>
	
		<p>Những người phát triển các phần mềm ứng dụng  với những ý tưởng cực kì sắc bén nhưng không có đủ nguồn lực để xây dựng và quảng bá những điều đó.</p>
		<p>Và rất nhiều những đối tượng khác, những người có một lý do để có thể tạo cảm hứng cho công chúng.		</p>
      </div>
    </li>
    <li class="faq">
      <h4 id="WhyuseIg">Tại sao lại sự dụng IG9?</h4>
      <div class="faq_answer">
        <p>•	Vốn tài trợ:</p>
				<p>Đây là cách tốt nhất và hiệu quả nhất để bạn thu hút vốn cho dự án của bạn. Ý tưởng rất đơn giản – Người ta, một cách truyền thống, thường huy động nguồn vốn trước tiên từ những nguồn gần gũi nhất như bạn bè và gia đình. IG9 cũng hoạt động giống như vậy, trừ việc chúng tôi giúp bạn tiếp cận tới hàng triệu người ủng hộ trên internet.</p>

        <p>•	Hình ảnh/Nhận thức:</p>
				<p>Hầu hết các ý tưởng hay dự án thất bại không phải vì không thu hút được vốn. Chúng thất bại vì không thể tạo dựng được một hình ảnh vững chắc đối với khách hàng.
				Bằng cách đưa dự án của bạn đến với IG9, bạn có thể chắc chắn rằng hàng trăm, hàng ngàn người tiêu dùng có thể trực tiếp biết đến ý tưởng của bạn thông qua những liên kết của chúng tôi với các ngân hàng hay những website hàng đầu. Đây là cách tốt nhất để bạn quảng bá cho dự án của mình.				</p>

        <p>•	Chi phí hiệu quả:</p>
				<p>o	Cách ít tốn kém nhất để bạn thu hút vốn đầu tư – không lãi xuất, không chia sẻ quyền sở hữu.</p>
				<p>o	Cách ít tốn kém nhất để bạn tạo dựng hình ảnh dự án trước công chúng – Điều thậm chí quan trọng hơn cả vốn đầu tư.</p>
        <p>•	Những cơ hội và sự kết nối:</p>
				<p>o	IG9 nỗ lực hết sức để đảm bảo sự thành công cho dự án của bạn. Chúng tôi cung cấp các dịch vụ gia tăng giá trị  trước, sau và trong quá trình thu hút vốn của bạn. Chúng tôi kết nối bạn tới những sự giúp đỡ cần thiết từ những chuyên gia kinh tế, các nhà cung cấp, nguồn nhân lực… để chắp cánh cho ước mơ của bạn.</p>
				<p>o	Đây là cách tốt nhất để bạn tung ra sản phẩm hay dự án của mình, giúp bạn có được sự chú ý và quan tâm lớn từ cộng đồng.</p>
				<p>o	Ngay cả khi việc thu hút vốn không thành công, bạn vẫn có thể có được danh tiếng tốt. Danh tiếng đó có thể giúp bạn ảnh hưởng tới các nhà đầu tư hay công ty chuyên tìm kiếm các ý tưởng đắt giá.</p>
        <p>•	Một cái nhìn sâu sắc về thị trường:</p>
				<p>o	Đưa dự án của bạn đến với IG9 cho phép bạn trực tiếp tìm hiểu về chính thị trường sản phẩm của bạn, những nhu cầu của khách hàng. Bằng cách chia sẻ ý tưởng về dự án/sản phẩm của mình, bạn có thể biêt được nhu cầu của khách hàng về sản phẩm ra sao - Vấn đề vốn làm đau đầu những người xây dựng dự án.</p>
				<p>o	Ở IG9, bạn có thể tiếp cận trực tiếp tới những khách hàng cuối cùng và thu hút vốn từ chính họ, không cần thông qua các tổ chức đầu tư mạo hiểm hay ngân hàng.</p>
