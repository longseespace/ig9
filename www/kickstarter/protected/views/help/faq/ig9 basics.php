        <p>Có nhưng việc đó chỉ nhằm mục đích xem xét dự án có tuân theo <a href="/help/guidelines" target="_blank">Hướng dẫn dự án</a> hay không. IG9 không điều tra khả năng hoàn thành dự án của người sáng lập dự án. Những người góp vốn chính là những người quyết định giá trị và sự xứng đáng của dự án thông qua việc góp vốn hay không. </p>
      </div>
    </li>
    <li class="faq">
      <h4 id="WhoIsRespForCompAProjAsProm"><strong>Những ai sẽ phải chịu trách nhiệm hoàn thành dự án như lời hứa?</strong></h4>
      <div class="faq_answer">
        <p>Trách nhiệm đó hoàn toàn là của những người sáng lập dự án. IG9 không liên quan đến việc phát triển dự án của họ. </p>
      </div>
    </li>
    <li class="faq">
      <h4 id="WhatShouCreaDoIfTheyHaviProbCompTheiProj"><strong>Những người sáng lập dự án sẽ phải làm gì nều họ gặp phải khó khăn trong  việc hoàn thành dự án?</strong></h4>
      <div class="faq_answer">
        <p>Nều có vấn đề xảy ra, những người sáng lập nên  cập nhật thông tin về dự án của họ (các thông tin cập nhật sẽ được gửi cho  những người tài trợ qua email) giải thích rõ vấn đề. Việc chia sẻ những câu  truyện, những chặng đường, khó khăn, thách thức là rất quan trọng. Vì hầu hết  những nhà tài trợ ủng hộ cho dự án của bạn vì cái họ thực sự muốn là nhìn thấy  một điều gì đó sảy ra và họ muốn trở thành một phần của điều đó. Những người  sáng lập thể hiện sự cố gắng và nhiệt tình một cách minh bạch thường sẽ nhận  được sự cảm thông từ phía những người ủng hộ.</p>
        <p>Không hiếm trường hợp các dự án phải kéo dài hơn  mong đợi. Đôi khi việc tiến hành các công việc trên thực tế gặp khó khăn hơn so  với dự liệu. Nếu những người xây dựng dự án thực sự nỗ lực và minh bạch trong  công việc của mình, những người tại trợ nên kiên nhẫn  thông cảm và tin tưởng những người xây dựng dự  án.</p><p>Nếu như vấn  đề nghiêm trọng đến mức người chủ dự án không còn có thể thực hiện được dự án  của họ, họ phải tìm ra cách giải quyết. Các bước có thể bao gồm đề nghị hoàn  lại tiền cho những người tài trợ, đưa ra giải trình chi tiết về việc sử dụng  vốn và những việc làm khác để làm vừa lòng các nhà tài trợ.</p>
      </div>
    </li>
    <li class="faq">
      <p><strong>Những người sáng lập dự án có phải chịu ràng buộc  pháp lý trong việc thực hiện các lời hứa trong dự án của họ không</strong></p>
      <div class="faq_answer">
        <p>Có. Trong điều khoản sử dụng của IG9 có yêu  cầu các nhà sáng lập dự án phải thực hiện tất cả những lời hứa về phần thưởng  hay hoàn trả lại tiền cho bất cứ nhà tài trợ nào không nhận được phần thưởng vì  bất cứ lý do nào. Chúng tôi xây dựng các điều khoản này để tạo dựng một yêu cầu  pháp lý bắt buộc các nhà tạo lập dự án tuân theo trong dự án của họ và cho  những nhà tài trợ quyền được bồi thường nếu có sai phạm sảy ra.</p>
      </div>
    </li>
    <li class="faq">
      <p><strong>Liệu IG9 có thể hoàn lại tiền cho những người  tài trợ nếu như dự án không thể hoàn thành</strong></p>
      <div class="faq_answer">
        <p>IG9  không chịu trách nhiệm pháp lý hoàn lại tiền cho những người tài trợ nếu tiền  tài trợ đã được gửi đến cho chủ dự án. Trên thực tế, IG9 không hề lưu giữ  tiền tại trợ. Mọi trao đổi đểu được thực hiện giữa người sáng lập dự án và các  nhà tại trợ và quỹ tài trợ được giữ bởi ngân hàng/bên thứ ba là đối tác của  chúng tôi cho đến khi người sáng lâp dự án thực hiện lời hứa. Nếu người tạo  dựng dự án không thể hiện uy tín trong việc thực hiện lời hứa, quỹ tài trợ sẽ  được chia lại và trả về cho những người tài trợ. Nhưng xin hãy hiểu rằng điều  này chỉ phục vụ mục đích như một bước đảm bảo từ phía ngân hàng. Tại IG9,  chính những nhà tài trợ là người quyết định cuối cùng về tính hợp lệ và xứng  đáng cho một dự án bằng cách đầu tư hoặc không đầu tư cho dự án đó.</p>
      </div>
    </li>
    <li class="faq">
      <h4 id="WhatIsKickDoinAbouFulf">IG9 bảo vệ những nhà tài trợ như thế nào đối với các dự án đã thu được tài trợ nhưng không thực hiện lời hứa?</h4>
      <div class="faq_answer">
        <p>Những khoản đóng góp của những người tài trợ sẽ được giữ tại một quỹ của ngân hàng đối tác của chúng tôi. Thông thường, tiền sễ chỉ được giải ngân khi những chủ dự án thực hiện lời hứa.</p>
