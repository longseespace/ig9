      <div class="content">
        <p>Nếu như bạn giống chúng tôi thì việc đầu tiên khi bạn ghé qua một trang dự án là bấm nút play. Một Video là cách tốt nhất để có được một cảm giác, động lực, và tính chất của một dự án. Nó có thể trình bày những cách thức và là một tín hiệu để bạn dự đoán thành công của một dự án. Theo như website Kickstarter.com của Mỹ, Những dự án có video thường có tỷ lệ thành công cao hơn hẳn các dự án không có (50% so với 30%)</p>
        <p>Chúng tôi biết việc tạo một Video có thể khó khăn. Không có nhiều người trong chúng ta thích đứng trước camera. Nhưng chúng tôi cũng biết rằng việc tạo Video của riêng bạn là một thử thách xứng đáng vì nó cho thấy bạn thật sự quan tâm đến điều bạn làm. Đó là một sự mạo hiểm nhỏ cho một phần thưởng lớn.</p>

        <p></p>
        <p>
          Bạn chỉ cần máy tính kết nối mạng và có lòng nhiệt tình là đủ. Một vài Video có thể rất hoành tráng, sô khác có thể rất dài nhưng hầu hết, các Video đơn giản chỉ là ai đó kể câu chuyện của họ qua camera. Bạn có thể dành cả ngày quay phim và chỉnh sửa, hoặc nhờ trợ giúp từ vài người bạn khi rảnh rỗi. Video không cần phải hoàn hảo, chỉ cần Video đó là của chính bạn.
        </p>
        <p>Cho dù Video của bạn có sáng tạo đến đâu hay súc tích thế nào, hãy nhớ:</p>
        <ul>
          <li> Giới thiệu về bản thân bạn </li>
		<li> Giới thiệu câu truyện về dự án của bạn. Vì sao bạn có ý tưởng đó? Dự án đang ở giai đoạn nào? Bạn cảm thấy như thế nào về nó?</li>
		<li>Xin sự ủng hộ từ mọi người, giải thích vì sao bạn cần họ và bạn sẽ làm gì với tiền tài trợ</li>
		<li>Nói về những phần thưởng tuyệt vời bạn giành cho họ (hãy sử dụng những hình ảnh cụ thể nếu bạn có thể)</li>
		<li>Hãy giải thích nếu bạn không đạt được mức vốn mục tiêu, bạn sẽ không được gì và kết cục buồn.</li>
		<li>Nói lời cảm ơn với mọi người</li>

        </ul>
        <p>Và đừng sợ lộ diện trên máy quay và để mọi người biết họ đang góp tiền cho ai vì bạn sẽ không ngờ được tác dụng của điều đó đâu.</p>
        <p>
          Và một điều cần chú ý nữa, đó là xin đừng sử dụng âm nhạc có bản quyền trên Video của bạn mà không được phép. Các vấn đề kiện cáo là hoàn toàn không tốt và chúng tôi gợi ý cho bạn một số tài nguyên nhạc miễn phí nếu bạn thấy cần
          <a href="http://www.soundcloud.com/creativecommons">SoundCloud</a>,
          <a href="http://vimeo.com/musicstore">Vimeo Music Store</a>,
          <a href="http://freemusicarchive.org">Free Music Archive</a>,
          and
          <a href="http://www.ccmixter.org">ccMixter</a>.
        </p>
        <p>Và một vài vấn đề kỹ thuật đó là: Video phải bằng hoặc nhỏ hơn 1000MB và thuộc các định dạng MOV, MPEG, AVI, MP4, 3GP, WMV hoặc FLV.</p>
