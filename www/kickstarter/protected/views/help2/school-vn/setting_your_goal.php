        <p>Ignite hoạt động dựa trên mô hình thu hút vốn toàn bộ hay là không, nghĩa là dự án sẽ được tài trợ hoàn toàn hoặc không ai mất tiền cả. Dự án phải đặt ra mức vốn mục tiên cần thu hút và thời gian mục tiêu để thu hút được số vốn đó. Không có công thức hoàn hảo cho hai mục tiêu này. Mọi dự án đều khác nhau, nhưng có một vài điều bạn cần nhớ.</p>
        <h3>TÌM HIỂU VỀ NGÂN SÁCH</h3>
        <p>Bạn thật sự cần bao nhiêu tiền? Bạn đang định thu hút toàn bộ ngân sách hay chỉ một phần? Bạn đã tính tới chi phí sản xuất các phần thưởng cho nhà tài trợ và chi phí vận chuyển tới họ chưa? Hãy nghiên cứu trước những vấn đề này để minh bạch với những người tài trợ, họ sẽ coi trọng điều này.</p>
        <h3>HÃY CÂN NHẮC MẠNG LƯỚI TRUYỀN THÔNG CỦA BẠN</h3>
        <p>
          Ignite không phải một nguồn cung tiền kì diệu. Vốn đến từ nhiều nguồn khác nhau như những người bạn của bạn, gia đình bạn, hay khán giả của bạn, những người quen trên mạng xã hội, hay thậm chí nếu dự án của bạn đủ tốt có thể thu hút những người bạn chưa từng biết trên mạng. Bạn xây dựng động lực như thế nào cho dự án là tùy ở bạn.
        </p>
        <h3>LỰA CHỌN MỤC TIÊU CỦA BẠN</h3>
        <p>Một khi bạn đã nghiên cứu về ngân sách và cân nhắc về đối tượng tài trợ, bạn sẽ phải đưa ra mục tiêu thu hút vốn của bạn. Vì cơ chế chỉ có thể thu hút đủ hoặc vượt quá mức vốn cần thiết hoặc không thu hút được gì. Hãy suy nghĩ bạn cần bao nhiêu tiền để hoàn thành dự án như lời hứa (và cũng cân nhắc xem bạn có thể thu hút được bao nhiêu vốn), và lựa chọn mức vốn phù hợp.</p>
        <h3>ĐẶT GIỚI HẠN THỜI GIAN THU HÚT VỐN</h3>
        <p>Dự án có thể giới hạn thời gian thu hút vốn trong vòng 60 ngày, tuy nhiên những dự án dài hơn chưa chắc đã tốt hơn. Theo những thống kê, những dự án kéo dài bằng hoặc ít hơn 30 ngày có tỷ lệ thành công cao hơn. Một dự án tại Ignite cần rất nhiều công sức để tiến hành, và những dự án ngắn hơn thường mang tính tin tưởng cao hơn và thu hút nhiều người tài trợ hơn. Những dự án kéo dài hơn mang ít tính cấp bách hơn (đối với người sáng lập), trì hoãn hơn và thường không thành công.</p>
