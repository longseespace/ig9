        <h2>Tạo phần thưởng</h2>
      </div>
      <div class="content">
        <p>Phần thưởng là những thứ người tài trợ sẽ nhận được đổi lấy việc ủng hộ cho một dự án. Điều quan trọng là những phần thưởng sáng tạo, hữu hình, giá cả phù hợp là chúng không nên bị phóng đại. Những dự án có mức phần thưởng quá lớn hay quá tẻ nhạt sẽ gặp khó khăn trong việc thu hút sự ủng hộ..</p>
        <h3>Quyết định phần thưởng</h3>
        <p>Mọi phần thưởng chính cho dự án nên là những thứ được tạo ra từ chính dự án. Nếu, thí dụ, dự án của bạn là để thu âm một album nhạc mới thì phần thưởng có thể là một đĩa CD cho người tài trợ khi dự án kết thúc. Những phần thưởng là để chắc chắn những người tài trợ sẽ được hưởng những lợi ích giống như chính những người lập dự án (bởi chính họ là những người góp phần khiến dự án thành công).</p>
        <p>Những kiểu phần thưởng thường gặp trên Ignite là:</p>
        <ul>
          <li>
            <em>Bản sao của sản phẩm</em>
            một album, đĩa DVD, một bản in của tác phẩm. Những món đồ này nên được định giá theo giá thị trường bán lẻ.
          </li>
          <li>
            <em>Một sản phẩm sáng tạo của sự hợp tác: </em>
            Ví dụ một người tài trợ xuất hiện như một anh hùng trong cuốn truyện, mọi nhà tài trợ được vẽ lên tranh tường hay một vài nhà tài trợ được vỗ tay cho bài hát của album.
          </li>
          <li>
            <em>Những trải nghiệm sáng tạo: </em>
            Một cuộc gặp mặt trực tiếp, một cuộc gọi điện từ tác giả, một bữa tối với những diễn viên hay một buổi hòa nhạc tại sân nhà bạn.
          </li>
          <li>
            <em>Những vật lưu niệm sáng tạo: </em>
            Những cặp kính, lời cảm ơn trong lời giới thiệu (phim), những bưu thiếp ý nghĩa chứa một câu chuyện.
          </li>
        </ul>
        <h3>Làm sao để định giá phần thưởng</h3>
        <p>Ignite không phải tổ chức từ thiện, đây là cộng đồng nơi mọi người có thể đưa ra dự án sáng tạo của mình và đề nghị những phần thưởng cho những người ủng hộ.</p>
        <p>Vậy hãy nghĩ đến những giá trị mà người ủng hộ có thể có. Giá trị không hẳn chỉ là giá thành của phần quà. Hãy tưởng tượng những trải nghiệm có tính độc nhất hay chỉ có số lượng hạn chế. Điều đó có thể linh hoạt tùy thuộc vào tập hợp những người ủng hộ. Nhưng nếu phần thưởng là những phần quà vật lý, bạn cũng nên cân nhắc đến giá thành trên thị trường.</p>
        <p>Hoàn toàn không có nguyên tác nhất định phải tuân theo và chúng tôi khuyến khích bạn thể hiện sự sáng tạo và thực tế của bạn trong các dự án hết mức có thể. Hãy đặt mình vào vị trí những người tài trợ và tự hỏi vì sao bạn lại tài trợ cho dự án đó? Nếu trả lời được điều đó, bạn sẽ biêt được rất nhiều về tiềm năng dự án của bạn.</p>
