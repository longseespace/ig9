    <h1>Project Guidelines</h1>
    <p>IG9 is a funding platform for creative projects — everything from traditional forms of art (like theater and music) to contemporary forms (like design and games). These guidelines explain IG9’s focus. Projects violating these guidelines will not be allowed to launch.</p>
    <p>Note that as you go through the site you may find past projects on IG9 that conflict with these rules. We’re making tweaks as we learn and grow. Thanks for reading!</p>
    <div class="NS_help__project_guidelines">
      <ul class="list-guidelines">
        <li>
          <strong>1. Funding for
            <em>
              projects
            </em>
            only.</strong>
          A project has a clear goal, like making an album, a book, or a work of art. A project will eventually be completed, and something will be produced by it. A project is not open-ended. Starting a business, for example, does not qualify as a project.
        </li>
        <li>
          <strong>2. Projects must fit IG9’s categories.</strong>
          We currently support projects in the categories of Art, Comics, Dance, Design, Fashion, Film, Food, Games, Music, Photography, Publishing, Technology, and Theater.
          <p>
            <strong>Design and Technology projects have a few additional guidelines.</strong>
            If your project is in either of these categories, be sure to review them carefully.
          </p>
          <div id="more-guidelines-container">
            <p>
              IG9 requires additional information from Design and Technology projects so backers can make informed decisions about the projects they support. These requirements include detailed information about the creator’s background and experience, a manufacturing plan (for hardware projects), and a functional prototype.
            </p>
            <p>
              Additionally, not everything that involves design or technology is permitted on IG9. While there is some subjectivity in these rules, we’ve adopted them to maintain our focus on creative projects.
            </p>
            <ul>
              <li>
                <strong>
                  Projects, projects, projects.
                </strong>
                As in all categories, IG9 is for projects that can be completed, not things that require maintenance to exist. This means no e-commerce sites, web businesses, or social networking sites. (Yes, this means IG9 wouldn’t be allowed on IG9. Funny, but true.)
              </li>
              <li>
                <strong>
                  D.I.Y.
                </strong>
                We love projects from the hacker and maker communities (weekend experiments, 3D printers, CNC machines), and projects that are open source (hardware and software). Software projects should be run by the developers themselves.
              </li>
              <li>
                <strong>
                  Form as well as function.
                </strong>
                IG9 is a place for products with strong aesthetics. Think something you would find in a design store, not “As-Seen-On-TV” gizmos.
              </li>
            </ul>

          </div>
        </li>
        <li>
          <strong>3. Prohibited uses:</strong>
          <ul>
            <li>
              <strong>
                No charity or cause funding.
              </strong>
              Examples of prohibited use include raising money for the Red Cross, funding an awareness campaign, funding a scholarship, or promoting the donation of funds raised, or future profits, to a charity or cause.
            </li>
            <li>
              <strong>
                No "fund my life" projects.
              </strong>
              Examples include projects to pay tuition or bills, go on vacation, or buy a new camera.
            </li>
            <li>
              <strong>
                Prohibited content.
              </strong>
              There are some things we just don't allow on IG9.
              <div id="prohibited-container">
                <ul class="prohibited_projects">
                  <li>Alcohol (prohibited as a reward)</li>
                  <li>Automotive products</li>
                  <li>Baby products</li>
                  <li>Bath and beauty products</li>
                  <li>Contests (entry fees, prize money, within your project to encourage support, etc)</li>
                  <li>Cosmetics</li>
                  <li>Coupons, discounts, and cash-value gift cards</li>
                  <li>Drugs, drug-like substances, drug paraphernalia, tobacco, etc</li>
                  <li>Electronic surveillance equipment</li>
                  <li>Energy drinks</li>
                  <li>Exercise and fitness products</li>
                  <li>Financial incentives (ownership, share of profits, repayment/loans, etc)</li>
                  <li>Firearms, weapons, and knives</li>
                  <li>Health and personal care products</li>
                  <li>Heating and cooling products</li>
                  <li>Home improvement products</li>
                  <li>Infomercial or As-Seen-on-TV type products</li>
                  <li>Medical and safety-related products</li>
                  <li>Multilevel marketing and pyramid programs</li>
                  <li>Nutritional supplements</li>
                  <li>Offensive material (hate speech, inappropriate content, etc)</li>
                  <li>Pet supplies</li>
                  <li>Pornographic material</li>
                  <li>Projects endorsing or opposing a political candidate</li>
                  <li>Projects promoting or glorifying acts of violence</li>
                  <li>Projects using IG9 simply to sell existing inventory</li>
                  <li>Raffles, lotteries, and sweepstakes</li>
                  <li>Real estate</li>
                  <li>Rewards in bulk quantities (more than ten of an item)</li>
                  <li>Rewards not directly produced by the project or its creator (no offering things from the garage, repackaged existing products, weekends at the resort, etc)</li>
                  <li>Self-help books, DVDs, CDs, etc</li>
                </ul>

              </div>
            </li>
          </ul>
        </li>
      </ul>
    </div>

    <hr>
    <h1>Community Guidelines</h1>
    <p class="header">
      <strong>Spread the word but don't spam</strong>
    </p>
    <p>
      Sharing your project with friends, fans, and followers is one thing, but invading inboxes and social networks uninvited is another. Spamming makes you and every other IG9 project look bad, and it puts your project in jeopardy of being suspended. Don’t do it.
    </p>
    <p>
      What's spam?
    </p>
    <ul>
      <li>
        Emailing or direct messaging strangers about your project
      </li>
      <li>
        Using email lists from third parties or soliciting backers of other IG9 projects
      </li>
      <li>
        Sending unsolicited @-replies on Twitter
      </li>
      <li>
        Link-bombing forums
      </li>
      <li>
        Promoting a project on other projects' pages
      </li>
    </ul>
    <p>
      Not sure if something is spam? If you have to ask, it probably is.
    </p>
    <p class="header">
      <strong>Be respectful and considerate</strong>
    </p>
    <p>
      This means:
    </p>
    <ul>
      <li>
        Don’t harass or make personal attacks.
      </li>
      <li>
        Don't post obscene, hateful, or objectionable content.
      </li>
      <li>
        Don't post copyrighted content without permission. Only post content that you have the rights to.
      </li>
      <li>
        If you don't like a project, don't back it. No need to be a jerk.
      </li>
    </ul>
    <p>
      Violating these rules or our <a href="/terms-of-use">Terms of Use</a> may result in your account being deleted and/or your project being suspended. That's not fun for anyone, so please follow these simple rules very carefully. Cool? Cool.
    </p>

