          <p>Có thể  bạn biết người sáng lập dự án hoặc bạn nghe về người này từ một nguồn thông tin đáng tin cậy.</p><p> Có thể họ có một Video tự tạo thuyết phục. Ví dụ như một người nổi tiếng xuất hiện trên video và nói về dự án.</p><p>Nếu bạn vẫn không chắc, hãy tìm hiểu phần tiểu sử của tác giả dự án trong trang dự án. Họ có tài khoản Facebook không? Họ có đưa ra những liên kết ngoài mang tính kiểm chứng để củng cố niềm tin cho bạn không? Hãy tận dụng khả năng tìm kiếm thông tin tuyệt vời của internet vì lợi ích của bạn.</p>
        </div>
      </li>
      <li class="faq">
        <h4 id="WhatDoIDoIfIHaveQuesAbouAProj">Tôi sẽ phải làm gì khi có thắc mắc về dự án?</h4>
        <div class="faq_answer">
          <p>Hãy hỏi chính người sáng lập dự án! Ở phía dưới cùng của mỗi trang dự án sẽ có nút "Đặt câu hỏi". Tính năng này sẽ cho phép bạn gửi trực tiếp thắc mắc của mình tới chủ dự án.</p><p>Nếu như bạn đã ủng hộ dự án đó và bạn muốn đặt câu hỏi công khai, bạn có thể đăng bình luận về dự án. Người sáng lập dự án sẽ được thông báo bằng email khi bạn làm vậy.</p>
        </div>
      </li>
    </ol>
  </li>
