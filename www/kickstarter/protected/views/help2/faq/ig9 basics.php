        <p>Cũng có tính năng cập nhật thông tin RSS tổng thể dành cho các dự án nổi bật được khuyến nghị, Bạn có thể đăng ký tại đây: <a href="http://www.ig9.com/projects/feed.atom" target="_blank">http://www.ig9.com/projects/feed.atom</a></p>
      </div>
    </li>
    <li class="faq">
      <h4 id="HowCanIContKick">Làm sao để tôi liên lạc với IG9?</h4>
