        <h2>Project Updates</h2>
      </div>
      <div class="content">
        <p>Project updates serve as your project’s blog. They’re a great way to share your progress, post media, and thank your backers. Posting a project update automatically sends an email to all your backers with that update. You can choose to make each update public for everyone to see, or reserve it for just your backers to view.</p>
        <h3>Building momentum</h3>
        <p>While your project is live and the clock ticking, keep your backers informed and inspired to help you spread the word. Instead of posting a link to your project and asking for pledges every day, treat your project like a story that is unfolding and update everyone on its progress. “Pics from last night’s show!” or “We found a printer for our book!” with a link to your project is engaging and fun for everybody to follow along with.</p>
        <h3>Sharing the process</h3>
        <p>Once your project is successfully funded, don’t forget about all the people that helped make it possible. Let backers and spectators watch your project come to life by sharing the decisions you make with them, explaining how it feels as your goal becomes a reality, and even asking them for feedback. Keeping backers informed and engaged is an essential part of IG9.</p>
        <h3>Celebrating success</h3>
        <p>Sharing reviews, press, and photos from your project out in the world — whether it’s opening night of your play or your book on someone’s bookshelf — is great for everyone involved. The story of your project doesn’t end after it gets shipped out. You still have a captivated audience that’s cheering for you. Communicating with them can be one of the most rewarding parts of the process.</p>
