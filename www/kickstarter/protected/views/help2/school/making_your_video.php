        <h2>Making Your Video</h2>
      </div>
      <div class="content">
        <p>If you’re like us, the first thing you do when visiting a project page is click play. A video is by far the best way to get a feel for the emotions, motivations, and character of a project. It’s a demonstration of effort and a good predictor of success. Projects with videos succeed at a much higher rate than those without (50% vs. 30%).</p>
        <p>We know that making a video can be intimidating. Not many of us like being in front of a camera. We also know that making a video is a challenge worth taking on. It says you care enough about what you’re doing to put yourself out there. It's a small risk with a big reward.</p>

        <p></p>
        <p>
          If you have computer access and a ready supply of enthusiasm, you’ve got all you need. Some videos are
          <a href="http://www.ig9.vn/projects/jonathanmann/song-a-day-the-album">big montages</a>
          and others are
          <a href="http://www.ig9.vn/projects/2115598587/i-am-i-feature-film">epic long takes</a>,
          but most videos are just someone telling their story straight into the camera. You can spend days shooting and editing, or you can just knock it out with a couple friends on a Saturday. It doesn't have to be perfect, it just has to be <i>you</i>.
        </p>
        <p>No matter how creative or bare-bones your video, you'll want to:</p>
        <ul>
          <li>Tell us who you are.</li>
          <li>Tell us the story behind your project. Where'd you get the idea? What stage is it at now? How are you feeling about it?</li>
          <li>Come out and ask for people's support, explaining why you need it and what you'll do with their money.</li>
          <li>Talk about how awesome your rewards are, using any images you can.</li>
          <li>Explain that if you don't reach your goal, you'll get nothing, and everyone will be sad.</li>
          <li>Thank everyone!</li>
        </ul>
        <p>And don't be afraid to put your face in front of the camera and let people see who they’re giving money to. We’ve watched thousands of these things, and you’d be surprised what a difference this makes.</p>
        <p>
          Another thing to remember: don't put any copyrighted music in your video without permission! Expensive lawsuits are never fun. Here are some music resources you can use when the time comes:
          <a href="http://www.soundcloud.com/creativecommons">SoundCloud</a>,
          <a href="http://vimeo.com/musicstore">Vimeo Music Store</a>,
          <a href="http://freemusicarchive.org">Free Music Archive</a>,
          and
          <a href="http://www.ccmixter.org">ccMixter</a>.
        </p>
        <p>And finally a few technical specs: videos must be 1000MB or less and have a file type of MOV, MPEG, AVI, MP4, 3GP, WMV, or FLV.</p>
