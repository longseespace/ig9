        <h2>Creating Rewards</h2>
      </div>
      <div class="content">
        <p>Rewards are what backers receive in exchange for pledging to a project. The importance of creative, tangible, and fairly priced rewards cannot be overstated. Projects whose rewards are overpriced or uninspired struggle to find support.</p>
        <h3>Deciding what to offer</h3>
        <p>Every project’s primary rewards should be things made by the project itself. If the project is to record a new album, then rewards should include a copy of the CD when it’s finished. Rewards ensure that backers will benefit from a project just as much as its creator (i.e., they get cool stuff that they helped make possible!).</p>
        <p>There are four common reward types that we see on IG9:</p>
        <ul>
          <li>
            <em>Copies of the thing:</em>
            the album, the DVD, a print from the show. These items should be priced what they would cost in a retail environment.
          </li>
          <li>
            <em>Creative collaborations:</em>
            a backer appears as a hero in the comic, everyone gets painted into the mural, two backers do the handclaps for track 3.
          </li>
          <li>
            <em>Creative experiences:</em>
            a visit to the set, a phone call from the author, dinner with the cast, a concert in your backyard.
          </li>
          <li>
            <em>Creative mementos:</em>
            Polaroids sent from location, thanks in the credits, meaningful tokens that tell a story.
          </li>
        </ul>
        <h3>Deciding how to price</h3>
        <p>IG9 isn’t charity: we champion exchanges that are a mix of commerce and patronage, and the numbers bear this out. To date the most popular pledge amount is $25 and the average pledge is around $70. Small amounts are where it’s at: projects <i>without</i> a reward less than $20 succeed 35% of the time, while projects <i>with</i> a reward less than $20 succeed 54% of the time.</p>
        <p>So what works? Offering something of value. Actual value considers more than just sticker price. If it’s a limited edition or a one-of-a-kind experience, there’s a lot of flexibility based on your audience. But if it’s a manufactured good, then it’s a good idea to stay reasonably close to its real-world cost.</p>
        <p>There is no magic bullet, and we encourage every project to be as creative and true to itself as possible. Put yourself in your backers’ shoes: would <i>you</i> drop the cash on your rewards? The answer to that question will tell you a lot about your project’s potential.</p>
