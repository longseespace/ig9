        <h3>Picking your project image</h3>
        <p>Your project image is how you will be represented on IG9 and the rest of the web. Pick something that accurately reflects your project and that looks nice, too!</p>
        <h3>Writing your short description</h3>
        <p>Your short description appears in your project’s widget, and it’s the best place to quickly communicate to your audience what your project is about. Stay focused and be clear on what your project hopes to accomplish. If you had to describe your project in one tweet, how would you do it?</p>
