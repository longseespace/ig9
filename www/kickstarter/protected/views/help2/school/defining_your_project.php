      <div class="content">
        <p>
          Whether it’s a book, a film, or a piece of hardware, the one trait that every IG9 campaign shares is that it is a
          <em>project.</em>
          Defining what your IG9 project
          <em>is</em>
          is the first step for every creator.
        </p>
        <p>
          What are you raising funds to do? Having a focused and well-defined project with a clear beginning and end is vital. For example: recording a new album is a finite project
          —
          the project finishes when the band releases the album
          —
          but launching a music career is not. There is no end, just an ongoing effort. IG9 is open only to finite projects.
        </p>
        <p>With a precisely defined goal, expectations are transparent for both the creator and potential backers. Backers can judge how realistic the project’s goals are, as well as the project creator’s ability to complete them. And for creators, the practice of defining a project’s goal establishes the scope of the endeavor, often an important step in the creative process.</p>
        <p>IG9 thrives on these open exchanges and clear explanations of goals. Make sure your project does this!</p>
        <p>
          If you're unsure if your project is a good fit for IG9 (or if IG9 is a good fit for your project), we'd encourage you to read the IG9
          <a href="/help/guidelines">Project Guidelines</a>
          and peruse
          <a href="/discover/recommended">recommended</a>
          and
          <a href="/discover/successful">successful</a>
          projects in your project's category.
        </p>
