        <h2>Setting Your Goal</h2>
      </div>
      <div class="content">
        <p>IG9 operates on an all-or-nothing funding model where projects must be fully funded or no money changes hands. Projects must set a funding goal and a length of time to reach it. There’s no magic formula to determining the right goal or duration. Every project is different, but there are a few things to keep in mind.</p>
        <h3>Researching your budget</h3>
        <p>How much money do you need? Are you raising the full budget or a portion of it? Have you factored in the cost of producing rewards and delivering them to backers? Avoid later headaches by doing your research, and be as transparent as you can. Backers will appreciate it.</p>
        <h3>Considering your networks</h3>
        <p>
          IG9 is not a magical source of money. Funding comes from a variety of sources
          —
          your audience, your friends and family, your broader social networks, and, if your project does well, strangers from around the web. It’s up to you to build that momentum for your project.
        </p>
        <h3>Choosing your goal</h3>
        <p>Once you’ve researched your budget and considered your reach, you’re ready to set your funding goal. Because funding is all-or-nothing, you can always raise more than your goal but never less. Figure out how much money you need to complete the project as promised (while considering how much funding you think you can generate), and select an amount close to that.</p>
        <h3>Setting your project deadline</h3>
        <p>Projects can last anywhere from one to 60 days, however a longer project duration is not necessarily better. Statistically, projects lasting 30 days or less have our highest success rates. A IG9 project takes a lot of work to run, and shorter projects set a tone of confidence and help motivate your backers to join the party. Longer durations incite less urgency, encourage procrastination, and tend to fizzle out.</p>
