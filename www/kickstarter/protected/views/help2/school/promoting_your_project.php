        <h2>Promoting Your Project</h2>
      </div>
      <div class="content">
        <p>An exceptional project can lead to outpourings of support from all corners of the web, but for most projects, support comes from within their own networks and their networks’ networks. If you want people to back your project you have to tell them about it. More than once! And in a variety of ways! Here’s how:</p>
        <h3>Smart outreach</h3>
        <p>A nice, personal message is the most effective way to let someone know about your project. Send an email to your close friends and family so they can be first to pledge, then use your personal blog, your Facebook page, and your Twitter account to tune in everyone who’s paying attention. Don’t overwhelm with e-blasts and group messages, but be sure to remind your networks about your projects a few times throughout the course of its duration. Take the time to contact people individually. It makes a big difference.</p>
        <h3>Meeting up</h3>
        <p>Don’t be afraid to take your IG9 project out into the real world. Nothing connects people to an idea like seeing the twinkle in your eye when you talk about it. Host pledge parties, print posters or flyers to distribute around your community, and organize meetups to educate people about your endeavor. Be creative!</p>
        <h3>Stopping the presses</h3>
        <p>Contact your local newspaper, TV, and radio stations and tell them about your project. Seek out like-minded blogs and online media outlets to request coverage. Writers are always looking for stories to write about, and the media has a big soft spot for DIY success stories.</p>
        <h3>Keeping it real</h3>
        <p>Whatever channel you use to tell your project’s story, don’t spam. This includes posting your link on other IG9 project pages, @messaging people to beg for money on Twitter, link-bombing on Facebook, and generally nagging people you don’t already know. Over-posting can alienate your friends and fans, and it makes every other IG9 project look bad too. Don’t do it!</p>
