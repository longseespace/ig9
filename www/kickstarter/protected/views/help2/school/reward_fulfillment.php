        <h2>Reward Fulfillment</h2>
      </div>
      <div class="content">
        <p>Once your project has been completed, the crucial final step is fulfilling the rewards promised to backers. Sending out all of your rewards can feel overwhelming, but staying organized and having fun with it will make the process much simpler.</p>
        <h3>Getting backer info</h3>
        <p>Don’t worry about gathering your backers’ info until after your project is successfully funded. At that point the IG9 survey tool will help you create surveys to request whatever info you may need to deliver your rewards (mailing addresses, T-shirt sizes, etc). You can find this feature in your Backer Report. Backers are notified via email when you send out the survey, and their responses are automatically entered into your Backer Report, which can then be exported as an Excel-compatible spreadsheet.</p>
        <h3>Planning ahead</h3>
        <p>Remember to take shipping costs into account before you start your project. It may end up being a bigger part of your budget than you thought. Keep in mind how you'll send each reward, how much they weigh, how many require international shipping, and how you will lug all of them to the post office. Don’t forget hidden costs (bubble mailers) or emotional ones (filling out 100 customs forms).</p>
        <p>Do some research online. You can start with the basics like the <a href="http://pe.usps.com/text/dmm100/intro.htm">USPS mailing guide</a>, <a href="http://pe.usps.com/businessmail101/checklist/welcome.htm">checklists</a>, and <a href="http://pe.usps.com/businessmail101/getstarted/bulkMail.htm">bulk mail info</a>, and then find out about other options that might work better for you.</p>
        <h3>Communication, communication, communication</h3>
        <p>Fulfillment might not be the most romantic step in doing a IG9 project, but it means getting your project into your backers’ hands. Take pictures of your packing party. Tell horror stories from the post office (backers love seeing photos and videos of their rewards being packaged). Ask for your backers to let you know when their stuff arrives. Invite friends over to drink wine and lick stamps. Cherish what are some of the last moments of the first phase in the life of your project.</p>
